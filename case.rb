weather = "windy"

case weather
when "snowing"
 	 puts "Ooh! it's freezing, best wear a heavy coat!"
when "raining"
	puts "You need a rain coat and your fave pair of wellington boots!"
when "foggy"
	puts "You might want to wear something colorful today."
when "hot and humid"
	puts "Perhaps something white and light!"
else
	puts "Well, try not to get blown away!"
end
